// 変数の宣言・値に3と5の倍数の代入
let num = 15;

// 変数numを出力する（確認用)
console.log(num);


// 変数numの値が3と5の倍数の場合、「3と5の倍数です」という文字列を出力する
if (num % 3 == 0 && num % 5 == 0) {
  console.log('3と5の倍数です');
}

// 変数numの値が3のみの倍数の場合、「3の倍数です」という文字列を出力する
else if (num % 3 == 0) {
  console.log('3の倍数です');
}

// 変数numの値が5のみの倍数の場合、「5の倍数です」という文字列を出力する
else if (num % 5 == 0) {
  console.log('5の倍数です');
}

// それ以外の場合、変数numの値を出力する
else {
  console.log(num);
}



